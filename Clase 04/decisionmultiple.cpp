#include <iostream>

/* Estructura de decision multiple */

int main() {
	int x=2;
	switch (x){
		case 0: cout << "el valor es 0" << endl;
		break;
		case 1: cout << "el valor es 1" << endl;
		break;
		case 2: cout << "el valor es 2" << endl;
		break;
		case 3: cout << "el valor es 3" << endl;
		break;
	}
	return 0;
}
